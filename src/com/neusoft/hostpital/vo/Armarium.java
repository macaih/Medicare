package com.neusoft.hostpital.vo;

public class Armarium {
	private int armariumId;
	private String armariumNo;
	private String armariumName;
	private Double armariumPrice;
	public int getArmariumId() {
		return armariumId;
	}
	public void setArmariumId(int armariumId) {
		this.armariumId = armariumId;
	}
	public String getArmariumNo() {
		return armariumNo;
	}
	public void setArmariumNo(String armariumNo) {
		this.armariumNo = armariumNo;
	}
	public String getArmariumName() {
		return armariumName;
	}
	public void setArmariumName(String armariumName) {
		this.armariumName = armariumName;
	}
	public Double getArmariumPrice() {
		return armariumPrice;
	}
	public void setArmariumPrice(Double armariumPrice) {
		this.armariumPrice = armariumPrice;
	}
}
